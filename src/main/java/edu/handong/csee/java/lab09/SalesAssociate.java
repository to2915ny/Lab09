package edu.handong.csee.java.lab09;

public class SalesAssociate {
	
	private String name = "";
	private double sales;
	
	public void setName(String name) {
		this.name = name;
	}
	public void setSales(double sales) {
		this.sales = sales;
	}
	public double getSales()
	{
		return sales;
	}
	public String getName()
	{
		return name;
	}
	
}
